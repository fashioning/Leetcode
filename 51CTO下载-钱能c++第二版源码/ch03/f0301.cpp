//=====================================
#include<iostream>
using namespace std;
//-------------------------------------
int main(){
  for(int i=1; i<=10; ++i){
    cout<<string(10-i,' ');
    for(char ch=65; ch<64+2*i; ++ch)    // 应写ch='A';
      cout<<ch;
    cout<<endl;
  }
}//====================================

 