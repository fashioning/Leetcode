#include<iostream>
//-------------------------------------
int* fn(){
  int* ap = new int;      // ap所指向的内存空间开始有效，可以访问了
  return ap;
}//------------------------------------
int main(){
  int* bp=fn();
  *bp = 15;
  std::cout<<*bp<<"\n";
  delete[] bp;              // bp所指即ap所指，此处使该内存空间无效
}//====================================

 